function normalizeLanguage(language) {
  return messages[language] ? language : 'zh-CN'
}

export function translateStatic(key, language = getPreferences().language) {
  const lang = normalizeLanguage(language)
  return messages[lang]?.[key] || messages['zh-CN'][key] || key
}

export function useMobileI18n() {
  const language = ref(normalizeLanguage(getPreferences().language))

  const syncLanguage = event => {
    language.value = normalizeLanguage(event?.detail?.language || getPreferences().language)
  }

  onMounted(() => {
    window.addEventListener('elder-preferences-changed', syncLanguage)
  })

  onBeforeUnmount(() => {
    window.removeEventListener('elder-preferences-changed', syncLanguage)
  })

  const t = key => translateStatic(key, language.value)

  return {
    language: computed(() => language.value),
    t,
  }
}
